package entidades;

public class Empleados {
	private String nombre,apellidos,dni,titulacion;
	private int dia,mes,año,numSegu,numCuen;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getTitulacion() {
		return titulacion;
	}
	public void setTitulacion(String titulacion) {
		this.titulacion = titulacion;
	}
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAño() {
		return año;
	}
	public void setAño(int año) {
		this.año = año;
	}
	public int getNumSegu() {
		return numSegu;
	}
	public void setNumSegu(int numSegu) {
		this.numSegu = numSegu;
	}
	public int getNumCuen() {
		return numCuen;
	}
	public void setNumCuen(int numCuen) {
		this.numCuen = numCuen;
	}

}
